	local isUnique = ScriptGlobal_IsUniqueNumberTable(bagIndexList);
	if isUnique == 0 then
		return OR_ERROR;
	end

	for i = 1, 5 do
		if bagIndexList[i] ~= -1 then
			if LuaFnIsItemAvailable( sceneId, selfId, bagIndexList[i] ) < 1 then	-- Ê¹ÓÃÓÐÎÊÌâµÄÎïÆ·ÔòÍË³öÁ÷³Ì
				return OR_STUFF_LACK
